import { Bounce, toast } from "react-toastify";

export const toastError = (errorText) => {
  toast.error(errorText, {
    position: "top-right",
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "dark",
    transition: Bounce,
  });
};

